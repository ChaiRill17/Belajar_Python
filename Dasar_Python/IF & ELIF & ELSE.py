# ini adalah suatu kondisi sederhana menggunakan IF, ELIF, dan ELSE

nilai = 75
if nilai >= 85: # if kondisi pertama terpenuhi
    print("Nilai Anda A")
elif nilai >= 70: # jika kondisi pertama tidak terpenuhi, cek kondisi kedua
    print("Nilai Anda B")
else: # jika kedua kondisi tidak terpenuhi
    print("Nilai Anda C")