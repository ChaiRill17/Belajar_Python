# Operator Perbandingan
print(1 == 1)    # sama dengan
print(2 != 3)    # tidak sama dengan
print(4 > 2)     # lebih besar dari
print(2 < 5)     # lebih kecil dari
print(3 >= 3)    # lebih besar atau sama dengan
print(4 <= 5)    # lebih kecil atau sama dengan

# Operator perbandingan menghasilkan nilai boolean (True atau False)