# loop for adalah pengulangan dengan jumlah tertentu atau yang sudah diketahui
# contoh loop menggunakan for
for i in range(5): # akan mengulang dari 0 sampai 4
    print('Perulangan ke', i) 
    
# loop while adalah pengulangan dengan syarat tertentu
# contoh loop menggunakan while
angka = 0
while angka < 5: # akan mengulang selama angka kurang dari 5
    print('Angka saat ini:', angka)
    angka += 1 # menambahkan nilai angka sebesar 1 setiap kali loop berjalan